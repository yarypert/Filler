#include "libft.h"

void	ft_putascii(char nb)
{
	if (nb % 10 + '0' == '(')
	{
		ft_putstr("128");
		return ;
	}
	if (nb / 10 > 0)
		ft_putascii(nb / 10);
	ft_putchar(nb % 10 + '0');
}
